package cursojava.iniciando;

public class ClasseChar {

	public static void main(String[] args) {
		
		/* CHAR representa uma letra/caracter --- usamos aspas SIMPLES*/
		char pessoaFeminino = 'F';
		
		/*não posso colocar mais de uma letra, isso evita
		 *  que alguém faça alguma alteração no código além do f*/
		
				
				if (pessoaFeminino == 'F') {
					System.out.println("Feminino");
				}else {
					System.out.println("Masculino");
				}
	}
}

